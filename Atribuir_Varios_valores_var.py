# muitos valores para multiplas variaveis

# python permite atribuir valores a varias variaveis em uma linha

x, y, z = "Orange", "Banana", "Cherry"

print(x)
print(y)
print(z)

# Um valor para varias variaveis

#e voce pode atribuir o mesmo valor a varias variaveis em uma linha:

x = y = z = "Orange"
print(x)
print(y)
print(z)

