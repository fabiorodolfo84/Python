# variaveis em python
# variaveis são conteineres para armazenar valores de dados.

# Criando variaveis
# python não tem comando para declarar uma variavel.
# uma variavel e criada no momento em que voce atribui um valor a ela.

x = 5
y = "John"

print(x)
print(y)

# As variaveis nao precisam ser declaradas com nenhum tipo especifico e podem ate mudar de tipo depois de terem sido definidas.

x = 4
x = "Sally"

print(x)

# Fundição

# Se voce  deseja especificar o tipo de dados de uma variavel, isso pode ser feito com conversão.

x = str(3)
x = int(3)
z = float(3)

print(x)
print(y)
print(z)
