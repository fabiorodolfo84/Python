# Criando variaveis
# python nao tem comando para declarar variavel.
# uma variavel é criada no momento em que voce atribui um valor a ela.

x = 5
y = "Fabio"

print(x)
print(y)

# As variaveis não precisao ser declaradas com nenhum tipo especifico e podem ate mudar de tipo depois de terem sido definidas.

x = 4
x = "Fabio rodolfo"
print(x)

# fundição

# Se voce deseja especificar o tipo de dados de uma variavel, isso pode ser feito com conversão.

x = str(3)  # x será 3
y = int(3)  # y será 3
z = float(3)  # will be 3.0

print(x, y, z)
